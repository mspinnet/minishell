#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	i;
	int	res;
	int	num;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	if (n < 0 && n != -2147483648)
	{
		n = -n;
		write(fd, "-", 1);
	}
	i = 1;
	num = n;
	while ((n / 10) > 0 && n != -2147483648)
	{
		n = n / 10;
		i = i * 10;
	}
	while (i > 0 && n != -2147483648)
	{
		res = (num / i) + '0';
		num = num % i;
		write(fd, &res, 1);
		i = i / 10;
	}
}
