#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	int		j;
	char	*str;

	j = 0;
	while (s1[j])
		j++;
	str = (char*)malloc(sizeof(const char) * j + 1);
	if (NULL == str)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
